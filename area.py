"""
Write a Python program to calculate the area of a triangle:
Declare height and base and assign numbers to them. Calculate
area = 0.5 * base * height. Print a message displaying the
dimensions and the area.
"""

# declare height and base and assign numbers to them
height = 7
base = 4

# calculate area = 0.5 * base * height
area = 0.5 * base * height


# print a message displaying the dimensions and the area
print("A triangle with a base of", base, "and a height of", height, "has an area of", area + ".")

